package org.gephi.forceAtlas2;

public class ForceAtlas2StrengthsLog {

    private String id;
    private String label;
    private double x;
    private double y;
    private double repulsionDx;
    private double repulsionDy;
    private double attractionDx;
    private double attractionDy;
    private double customForceDx;
    private double customForceDy;
    private double actualDx;
    private double actualDy;

    public ForceAtlas2StrengthsLog() {
    }

    //Getters and setters


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getRepulsionDx() {
        return repulsionDx;
    }

    public void setRepulsionDx(double repulsionDx) {
        this.repulsionDx = repulsionDx;
    }

    public double getRepulsionDy() {
        return repulsionDy;
    }

    public void setRepulsionDy(double repulsionDy) {
        this.repulsionDy = repulsionDy;
    }

    public double getAttractionDx() {
        return attractionDx;
    }

    public void setAttractionDx(double attractionDx) {
        this.attractionDx = attractionDx;
    }

    public double getAttractionDy() {
        return attractionDy;
    }

    public void setAttractionDy(double attractionDy) {
        this.attractionDy = attractionDy;
    }

    public double getCustomForceDx() {
        return customForceDx;
    }

    public void setCustomForceDx(double customForceDx) {
        this.customForceDx = customForceDx;
    }

    public double getCustomForceDy() {
        return customForceDy;
    }

    public void setCustomForceDy(double customForceDy) {
        this.customForceDy = customForceDy;
    }

    public double getActualDx() {
        return actualDx;
    }

    public void setActualDx(double actualDx) {
        this.actualDx = actualDx;
    }

    public double getActualDy() {
        return actualDy;
    }

    public void setActualDy(double actualDy) {
        this.actualDy = actualDy;
    }
}
